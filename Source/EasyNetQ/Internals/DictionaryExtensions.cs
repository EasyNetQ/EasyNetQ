using System.Collections.Generic;
using System.Linq;

namespace EasyNetQ.Internals
{
    /// <summary>
    ///     This is an internal API that supports the EasyNetQ infrastructure and not subject to
    ///     the same compatibility as public APIs. It may be changed or removed without notice in
    ///     any release. You should only use it directly in your code with extreme caution and knowing that
    ///     doing so can result in application failures when updating to a new EasyNetQ release.
    /// </summary>
    public static class DictionaryExtensions
    {
        /// <summary>
        ///     This is an internal API that supports the EasyNetQ infrastructure and not subject to
        ///     the same compatibility as public APIs. It may be changed or removed without notice in
        ///     any release. You should only use it directly in your code with extreme caution and knowing that
        ///     doing so can result in application failures when updating to a new EasyNetQ release.
        /// </summary>
        public static string Stringify(this IDictionary<string, object> source)
        {
            return string.Join(", ", source.Select(kvp => $"{kvp.Key}={kvp.Value}"));
        }

        /// <summary>
        ///     This is an internal API that supports the EasyNetQ infrastructure and not subject to
        ///     the same compatibility as public APIs. It may be changed or removed without notice in
        ///     any release. You should only use it directly in your code with extreme caution and knowing that
        ///     doing so can result in application failures when updating to a new EasyNetQ release.
        /// </summary>
        public static void UnionWith(this IDictionary<string, object> source, IDictionary<string, object> addendum)
        {
            if (addendum == null) return;

            foreach (var kvp in addendum)
            {
                source[kvp.Key] = kvp.Value;
            }
        }
    }
}
